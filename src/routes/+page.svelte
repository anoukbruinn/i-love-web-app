			<h2 class="Yolijn-name">YOLIJN VAN DER KOLK</h2>

			<div class="paragraph-Yolijn">
				<p>
					Yolijn van der Kolk deelde in de We Love Web-blog over design systems, die fungeren als
					een soort handleiding voor consistent design. Deze systemen zijn als gereedschapskisten
					voor ontwerpers en ontwikkelaars, met richtlijnen voor verschillende aspecten zoals
					teksten, microcopy en betrokkenheid van contentschrijvers.
				</p>
				<p>
					Ze benadrukte het belang van een design system dat voortkomt uit inzichten uit
					gebruikersonderzoek. Yolijn legde de nadruk op het testen met gebruikers, vooral gericht
					op toegankelijkheid, met een voorbeeld van een test met Stichting Accessibility.
				</p>
				<p>
					Ze illustreerde dit met voorbeelden zoals Sanoma Learning en het design system van ING,
					waarbij componenten breed toepasbaar zijn zonder de originele huisstijl te verliezen.
				</p>
				<p>
					Het NL Design System, gebruikt door meer dan 350 Nederlandse gemeentes en
					overheidsinstanties, laat zien hoe samenwerking tussen deze partijen en designers tot
					succes leidt. Dit systeem minimaliseert dubbel werk door gezamenlijke inspanningen en
					uitgebreide testen.
				</p>
				<p>
					Yolijn demonstreerde hoe dit systeem wordt toegepast in websites van steden zoals
					Amsterdam en Utrecht, waarbij ze specifieke elementen zoals breadcrumbs aanhaalde.
				</p>
				<p>
					Een belangrijk aspect hierbij was de EU Public Licence, die niet vereist dat alles open
					source is en zorgt voor duidelijke CSS-componenten met begrijpelijke namen voor eenvoudig
					gebruik.
				</p>
				<p>
					Er werd ook nadruk gelegd op design tokens, gebruikt via JSON om consistentie tussen
					design en CSS te waarborgen. Dit zorgt voor harmonie tussen het ontwerpen en bouwen en
					heeft aandacht voor toegankelijkheid, zoals het idee om interactieve elementen een
					minimale grootte te geven voor gebruikers met beperkte motoriek.
				</p>
				<p>
					Haar interesse in toegankelijkheid kwam naar voren door haar "toegankelijkheid bingo" en
					uitgebreide kennis, hoewel ze dit keer niet diep op dit aspect inging om binnen de
					presentatietijd te blijven.
				</p>
				<p>
					Tot slot werd waarde gehecht aan het beschermen van intellectueel eigendom, met speciale
					licenties om het gebruik van huisstijlen te reguleren en kopiëren te voorkomen.
				</p>
