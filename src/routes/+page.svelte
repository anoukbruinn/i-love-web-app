		<div class="Fenna">
			<h2 class="fenna-name">FENNA DE WILDE</h2>
			<div class="fenna-paragraph">
				<p>
					Fenna de Wilde, een talentvolle creative frondender bij Pakken & Bæck en voorheen werkzaam
					bij Build in Amsterdam, wierp tijdens haar presentatie "We Love Web" een fascinerende blik
					op de wereld van webontwikkeling. Met een indrukwekkende reeks projecten en haar
					betrokkenheid bij de ontwikkeling van de cryptowallet Phantom, bracht ze niet alleen haar
					werk in de schijnwerpers, maar deelde ze ook inzichten over het integreren van
					toegankelijkheid in Front-End Development
				</p>

				<p>
					De presentatie opende met een visuele reis door Fenna's portfolio, waarbij ze de aandacht
					vestigde op websites die een bijna kunstzinnige posterstijl omarmen. Een opvallend project
					was Phantom, waar Fenna bewust koos voor een lichtere en speelsere benadering in
					tegenstelling tot de donkere en complexe normen van cryptowallets. Ze streeft ernaar om
					nieuwe crypto-eigenaren uit te nodigen met een begrijpelijke en vriendelijke
					gebruikersinterface.
				</p>
				<p>
					Een centraal thema dat Fenna aansneed, was de kloof tussen esthetiek en toegankelijkheid
					op het web. Ze benadrukte het gebrek aan focus op toegankelijkheid in veel bedrijven,
					waarbij de nadruk vaak ligt op visueel aantrekkelijke animaties. Fenna deelde vervolgens
					haar toegankelijkheidsstrategieën en de belangrijkste stappen die ze volgt bij het creëren
					van websites.
				</p>

				<ul>
					<li>
						Focus state voor visueel beperkten: Zichtbare focus state voor gebruikers met visuele
						beperkingen.
					</li>
					<li>
						Aria-attributen: Gebruik van deze attributen om screen readers te assisteren bij het
						begrijpen van de content.
					</li>
					<li>
						Toegankelijke carousels: Erkenning van de uitdagingen bij het ontwikkelen van
						toegankelijke carousels.
					</li>
					<li>
						Modals met <dialog>
							: Correcte focus state-overdracht naar modals voor een betere gebruikerservaring.
						</dialog>
					</li>
					<li>
						Kleurcontrast en lettergrootte: Strikte naleving van WCGA-regels om kleurenblindheid te
						verminderen, samen met aandacht voor lettergrootte en regellengte.
					</li>
					<li>
						Alt-tags: Belang van het beschrijven van afbeeldingen, zelfs bij het gebruik van CMS.
					</li>
					<li>Correcte semantiek: Prioriteit voor een vlotte ervaring met screen readers.</li>
					<li>
						Skip to content button: Snelle navigatie naar de hoofdinhoud voor gebruikers die de
						header willen overslaan.
					</li>
					<li>
						Prefers reduced motion button: Optie voor gebruikers om animaties te verminderen of uit
						te schakelen.
					</li>
				</ul>

				<p>
					Naast haar toegankelijkheidsinzichten onthulde Fenna ook haar benadering van hergebruik in
					Front-End Development. Ze benadrukte de waarde van bestaande code en ontwerpelementen om
					efficiëntie te bevorderen.
				</p>
				<p>
					Fenna de Wilde's visie illustreert de kunst van webontwikkeling, waar creativiteit niet
					alleen wordt gevierd in het ontwerp, maar ook in de inclusiviteit van de digitale
					ervaring. Haar presentatie benadrukt het belang van een gebalanceerde aanpak in een steeds
					evoluerend landschap van webontwikkeling.
				</p>
			</div>
